# Calculate a Grade

def get_grade(get_grade)
	if 
		get_grade >= 90
			return 'A'
	end
	if 
		get_grade >= 80
			return 'B'
	end
	if 
		get_grade >= 70
			return 'C'
	end
	if 
		get_grade >= 60
			return 'D'
	end
	if 
		get_grade < 60
			return 'F'
	end
end


